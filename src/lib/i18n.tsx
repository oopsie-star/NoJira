type TranslationKey = keyof typeof translations.en

interface I18nContextValue {
  locale: Locale
  setLocale: (locale: Locale) => void
  t: (key: TranslationKey | string, vars?: Record<string, string | number>) => string
}

const I18nContext = createContext<I18nContextValue | null>(null)

function interpolate(template: string, vars?: Record<string, string | number>) {
  if (!vars) return template
  return Object.entries(vars).reduce(
    (result, [key, value]) => result.replace(new RegExp(`\\{${key}\\}`, 'g'), String(value)),
    template
  )
}

export function LanguageProvider({ children }: { children: ReactNode }) {
  const profile = useStore((state) => state.profile)
  const [locale, setLocaleState] = useState<Locale>(() => {
    const stored = window.localStorage.getItem(STORAGE_KEY)
    return stored === 'ru' ? 'ru' : 'en'
  })

  useEffect(() => {
    if (profile?.locale && profile.locale !== locale) {
      setLocaleState(profile.locale)
      window.localStorage.setItem(STORAGE_KEY, profile.locale)
    }
  }, [profile?.locale, locale])

  const setLocale = useCallback((nextLocale: Locale) => {
    setLocaleState(nextLocale)
    window.localStorage.setItem(STORAGE_KEY, nextLocale)
  }, [])

  const t = useCallback<I18nContextValue['t']>((key, vars) => {
    const localeTranslations = translations[locale] as Record<string, string>
    const defaultTranslations = translations.en as Record<string, string>
    const source = localeTranslations[key] ?? defaultTranslations[key] ?? key
    return interpolate(source, vars)
  }, [locale])

  const value = useMemo(() => ({ locale, setLocale, t }), [locale, setLocale, t])

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n() {
  const context = useContext(I18nContext)
  if (!context) throw new Error('useI18n must be used within LanguageProvider')
  return context
}
